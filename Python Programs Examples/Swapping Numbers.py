#======= Program To Swap Two Numbers With or Without Using Third Variable ======

'''
if a = 10 and b = 5 then result should be a = 5 and b = 10. 
'''

# Swapping Two Numbers Without using 3rd variable.

a = 10 
b = 5
a = a + b # a = 15
b = a - b # b = 10
a = a - b # a = 5

print(a,b)

